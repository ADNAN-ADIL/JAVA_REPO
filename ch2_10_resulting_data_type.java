package com.company;

public class ch2_10_resulting_data_type {
    public static void main(String[] args) {
        int i = 20;
        i++;
        System.out.println(i);
        float j =2.5f;
        System.out.println(++j);
        double k = 2.6744833;
        System.out.println(k--);
        System.out.println(k);
        int l = 24;
        System.out.println(--l);
        int y = 7;
        System.out.println(++y*8);
        char ch='a';
        System.out.println(++ch);

    }
}
